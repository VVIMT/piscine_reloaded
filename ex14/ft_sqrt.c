int		ft_sqrt(int nb)
{
	int		x;

	x = 1;
	if (nb > 2147483647 || nb <= 0)
		return (0);
	while (x * x < nb && x * x > 0)
		x++;
	if (x * x == nb)
		return (x);
	else
		return (0);
}
